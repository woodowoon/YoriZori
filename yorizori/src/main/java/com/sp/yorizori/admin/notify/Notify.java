package com.sp.yorizori.admin.notify;

public class Notify {
	private int recipeNum;
	private String userId;
	private String reason;
	private String notify_date;
	private String content;
	private String reg_date;
	private String adminId;
	
	private int recipecommentNum;
	private String procesDate;
	
	public int getRecipeNum() {
		return recipeNum;
	}
	public void setRecipeNum(int recipeNum) {
		this.recipeNum = recipeNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getNotify_date() {
		return notify_date;
	}
	public void setNotify_date(String notify_date) {
		this.notify_date = notify_date;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	public int getRecipecommentNum() {
		return recipecommentNum;
	}
	public void setRecipecommentNum(int recipecommentNum) {
		this.recipecommentNum = recipecommentNum;
	}
	public String getProcesDate() {
		return procesDate;
	}
	public void setProcesDate(String procesDate) {
		this.procesDate = procesDate;
	}
	
}
